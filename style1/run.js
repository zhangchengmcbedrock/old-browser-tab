document.addEventListener("keydown", function(event) {
    if (event.key === "1") {
      window.location.href = "https://www.baidu.com";
    } else if (event.key === "2") {
      window.location.href = "https://www.bing.com";
    } else if (event.key === "3") {
      window.location.href = "https://www.google.com/intl/zh-CN";
    }
  });